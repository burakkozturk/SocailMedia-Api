package platformApp.demo.service;

import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;
import org.springframework.stereotype.Service;
import platformApp.demo.entites.User;
import platformApp.demo.repositories.UserRepository;
import platformApp.demo.security.JwtUserDetails;

@Service
public class UserDetailServiceİmpl implements UserDetailsService {

    private UserRepository userRepository;

    public UserDetailServiceİmpl(UserRepository userRepository) {
        this.userRepository = userRepository;
    }

    @Override
    public UserDetails loadUserByUsername(String username) throws UsernameNotFoundException {
        User user = userRepository.findByUserName(username);
        return JwtUserDetails.create(user);
    }

    public UserDetails loadUserById(Long id){
        User user = userRepository.findById(id).get();
        return JwtUserDetails.create(user);
    }
}
